public class AuthorWithQuoteCount {
    private String name;
    private String wikiUrl;
    private int quoteCount;

    public AuthorWithQuoteCount() {
    }

    public String getName() {
        return name;
    }

    public String getWikiUrl() {
        return wikiUrl;
    }

    public int getQuoteCount() {
        return quoteCount;
    }
}
